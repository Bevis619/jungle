    /**
     * 计算传入数据的CRC校验值
     * @param puchMsg 需计算CRC校验值的数据
     * @param usDataLen 数据长度
     * @return crc校验值
     */
    public static int update(byte[] puchMsg, int usDataLen) {
        int uIndex;
        byte uchCRCHi = (byte) 0xFF;
        byte uchCRCLo = (byte) 0xFF;
        for (int i = 0; i < usDataLen; i++) {
            uIndex = (uchCRCHi ^ puchMsg[i]) & 0xff;
            uchCRCHi = (byte) (uchCRCLo ^ auchCRCHi[uIndex]);
            uchCRCLo = auchCRCLo[uIndex];
        }
        int value = ((((int) uchCRCHi) << 8 | (((int) uchCRCLo) & 0xff))) & 0xffff;
        return value;
    }

    /**
     * 计算数据CRC校验值,并将CRC校验值添加至数据包的末尾
     * @param sendContent 需计算校验值的数据包
     * @return 带CRC校验值的数据包
     */
    public static byte[] getSendBuf(byte[] sendContent) {
        int ri = update(sendContent, sendContent.length);
        byte[] updatedContent = new byte[sendContent.length + 2];
        System.arraycopy(sendContent, 0, updatedContent, 0, sendContent.length);
        updatedContent[updatedContent.length - 1] = (byte) (0xff & ri);
        updatedContent[updatedContent.length - 2] = (byte) ((0xff00 & ri) >> 8);
        return updatedContent;
    }

    /**
     * 获取CRC
     * 
     * @param sendContent 数据包
     * @return crc字节数组
     */
    public static byte[] getCrc(byte[] sendContent) {
        int ri = update(sendContent, sendContent.length);
        byte[] crc = new byte[2];
        crc[1] = (byte) (0xff & ri);
        crc[0] = (byte) ((0xff00 & ri) >> 8);
        return crc;
    }

    /**
     * 校验数据包的完整性，传入的数据包末尾包含CRC校验值，
     * 根据数据计算CRC值，并和数据包中的CRC校验值比较
     * @param bb 数据包，格式: data + 2字节CRC校验码
     * @return 数据包是否有效
     */
    public static boolean checkBuf(byte[] bb) {
        int ri = update(bb, bb.length - 2);
        if (bb[bb.length - 1] == (byte) (ri & 0xff) && bb[bb.length - 2] == (byte) ((0xff00 & ri) >> 8)) {
            return true;
        }
        return false;
    }

    /**
     * Check crc
     * 
     * @param data 数据包字节数组
     * @param crc crc字节数组
     * @return crc是否有效
     */
    public static boolean checkCRC(byte[] data, byte[] crc) {
        int ri = update(data, data.length);
        if (crc[1] == (byte) (ri & 0xff) && crc[0] == (byte) ((0xff00 & ri) >> 8)) {
            return true;
        }
        return false;
    }

